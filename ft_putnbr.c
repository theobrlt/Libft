#include "libft.h"

void	ft_putnbr(int n)
{
	long long int	x;

	x = n;
	x < 0 ? ft_putchar('-') : x;
	x = x < 0 ? x * -1 : x;
	x >= 10 ? ft_putnbr(x / 10) : x;
	x >= 10 ? ft_putnbr(x % 10) : x;
	(x >= 0 && x <= 9) ? ft_putchar(x + 48) : x;
}
